
# Puedes guardar datos dentro de una palabra clave (variable)
nombre = "Soter"

# Con esto puedes usar las funciones solo con la variable
print(nombre)

# Se puede guardar cualquier tipo de dato en una variable
numero = 4
lista = [20, 25]

# Case sensitive: Distingue de mayusculas y minusculas
game = "CSGO"
Game = "CSGO"
# No se puede anteponer un numero en una variable a menos que venga con un "_"
_2game = "CSGO"

# Se pueden crear variables en una sola linea con ",": x, y = "x", y Con x=String e y=Integer
word, number = "Hola", 74
# Puedes mostrar dos variables en una sola linea igual
print(word, number)

# Conventiones: Es la forma en que se le llama a una variable (para tener el codigo mas ordenado)
game_name = "CSGO"      # Snake Case
gameName = "Minecraft"  # Camel Case
GameName = "Mario"      # Pascal Case

# Constant: Es lo mismo que una variable solo que este no cambia, se escribe en mayuscula
PI = 3.141592654
MY_NAME = "Bayron"

# Python es de tipeado dinamico, es decir, que sus varaibles pueden cambiar facilmente
variable1 = "Hi"
variable1 = 450